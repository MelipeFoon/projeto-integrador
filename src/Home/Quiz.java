    private void botaoProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoProximoActionPerformed
       
        
        if(botaoProximo.getText().equals("Reiniciar o Quiz")){
            //Reiniciar o Quiz
            botaoProximo.setText("Próximo");
            index =0;
            numeroAcertos =0;
        }
        
        if(index ==perguntas.size()){
            calcularPontos();
            us.setPontos(numeroAcertos*mult);
            us.setRa(JOptionPane.showInputDialog("Digite o seu RA: "));
            cBD.consultarUser(us);
            cabecario.setText("Pontuação: "+us.getPontos());
            System.out.println(us.getRa());
            System.out.println(us.getNome());
            System.out.println(us.getPontos());
            cBD.incluirRank(us);
            
            botaoProximo.setText("Reiniciar o Quiz");

        }
        else{
            //habilitar os botões
            enableRButton(true);
        
            //Mostrar próxima pergunta
            cabecario.setText(perguntas.get(index));
            botaoRadio1.setText(opcoes[index][0]);
            botaoRadio2.setText(opcoes[index][1]);
            botaoRadio3.setText(opcoes[index][2]);
            botaoRadio4.setText(opcoes[index][3]);
            
            if(index ==perguntas.size()-1){
                botaoProximo.setText("Finalizar");
            }
        }
        if(botaoProximo.getText().equals("Finalizar")){
            pausarTimer=true;
        }
        else{
            pausarTimer=false;
        }
        //Limpar seleção
        bg.clearSelection();
    }//GEN-LAST:event_botaoProximoActionPerformed

