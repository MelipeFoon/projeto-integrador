    public void iniciarTimer() {
        Thread countdownThread = new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 300; i >= 0; i--) {
                    if (pausarTimer) {
                        // Pausar o timer
                        while (pausarTimer) {
                            try {
                                Thread.sleep(1000);
                            } catch (Exception e) {
                                System.out.println("Error: " + e);
                            }
                        }
                    }
                    System.out.println(i + " ");
                    setTempo(i);
                    try {
                        Thread.sleep(1000);
                    } catch (Exception e) {
                        System.out.println("Error: " + e);
                    }
                }
                System.out.println("\nFinished");
            }
        });
        countdownThread.start();
    }
    
